    private void cmbRutActionPerformed(java.awt.event.ActionEvent evt) {                                         
    // Habilita o deshabilita los campos según la selección de RUT
    if (cmbRut.getSelectedIndex() > 0) {
        txtPassword.setEnabled(true);
        txtId.setEnabled(true);
        cmbAcceso.setEnabled(true);
    } else {
        // Si no se selecciona un RUT válido, deshabilita los campos
        txtPassword.setEnabled(false);
        txtId.setEnabled(false);
        cmbAcceso.setEnabled(false);
    }
}

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Rut = cmbRut.getSelectedItem().toString();
        String Password = txtPassword.getText();
        String Id = txtId.getText();
        String Acceso = cmbAcceso.getSelectedItem().toString();

        Connection con = Conexion.getConexion();
        if (con != null) {
            try {
                // Encripta la contraseña antes de actualizarla en la base de datos
                String contraseñaEncriptada = PasswordHashing.encriptarPassword(Password);

                // Realiza la actualización en la tabla 'usuario' asociando el Rut seleccionado
                String consultaActualizacionUsuario = "UPDATE usuario SET Id = ?, Password = ?, Acceso = ? WHERE Rut_Usuario = ?";
                PreparedStatement pstmtActualizacionUsuario = con.prepareStatement(consultaActualizacionUsuario);

                pstmtActualizacionUsuario.setString(1, Id);
                pstmtActualizacionUsuario.setString(2, contraseñaEncriptada); // Actualiza la contraseña encriptada
                pstmtActualizacionUsuario.setString(3, Acceso);
                pstmtActualizacionUsuario.setString(4, Rut); // Actualiza el registro correspondiente al Rut seleccionado

                int filasAfectadas = pstmtActualizacionUsuario.executeUpdate();

                if (filasAfectadas > 0) {
                    JOptionPane.showMessageDialog(this, "Datos actualizados exitosamente.");

                    // Limpia los campos después de la actualización
                    txtPassword.setText("");
                    txtId.setText("");
                    cmbAcceso.setSelectedIndex(0);
                } else {
                    JOptionPane.showMessageDialog(this, "No se encontró un registro con el Rut seleccionado.");
                }

                pstmtActualizacionUsuario.close();
                con.close();
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error al actualizar datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo establecer la conexión a la base de datos.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

