    private void cargarRutUsuarios() {
        Connection con = Conexion.getConexion();
        if (con != null) {
            try {
                String consultaRutUsuarios = "SELECT Rut_Usuario FROM usuario";
                PreparedStatement pstmtRutUsuarios = con.prepareStatement(consultaRutUsuarios);
                ResultSet rsRutUsuarios = pstmtRutUsuarios.executeQuery();

                List<String> rutUsuarios = new ArrayList<>();
                while (rsRutUsuarios.next()) {
                    rutUsuarios.add(rsRutUsuarios.getString("Rut_Usuario"));
                }

                // Llena el JComboBox con los RUT de usuario disponibles.
                DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>(rutUsuarios.toArray(new String[0]));
                cmbRut.setModel(model);

                rsRutUsuarios.close();
                pstmtRutUsuarios.close();
                con.close();
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error al cargar los RUT de usuario: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo establecer la conexión a la base de datos.");
        }
}

