    public void llenarComboTarea() {
        try {
            cmbTarea.removeAllItems();

            String consulta = "SELECT NumTarea FROM Tarea";

            Connection con = Conexion.getConexion();
            java.sql.PreparedStatement pstmt = con.prepareStatement(consulta);

            ResultSet resultado = pstmt.executeQuery();
            while (resultado.next()) {
                String numTarea = resultado.getString("NumTarea");
                cmbTarea.addItem(numTarea);
            }

            resultado.close();
            pstmt.close();
            con.close();

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

