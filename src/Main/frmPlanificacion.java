    private void cmdIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdIngresarActionPerformed
       String NumOrden = cmbOrden.getSelectedItem().toString();
    String NumTarea = cmbTarea.getSelectedItem().toString();
    String NombreEmpleado = cmbEmpleado.getSelectedItem().toString(); // Cambié la variable a NombreEmpleado para mayor claridad

    // Validación de Campos Obligatorios
    if (NumOrden.isEmpty() || NumTarea.isEmpty() || NombreEmpleado.isEmpty()) {
        System.out.println("Todos los campos son obligatorios. Verifique e inténtelo nuevamente.");
        return; // No continuar con la inserción
    }

    try {
        // Obtener el NumEmpleado del empleado seleccionado
        String sqlGetNumEmpleado = "SELECT NumEmpleado FROM Empleado WHERE Nombre = ?";
        try (Connection con = Conexion.getConexion();
             PreparedStatement pstmtGetNumEmpleado = con.prepareStatement(sqlGetNumEmpleado)) {

            pstmtGetNumEmpleado.setString(1, NombreEmpleado);
            ResultSet resultSet = pstmtGetNumEmpleado.executeQuery();

            if (resultSet.next()) {
                String NumEmpleado = resultSet.getString("NumEmpleado");

                // Inserción de Datos
                String sql = "INSERT INTO Planificacion (NumOrden, NumTarea, NumEmpleado) VALUES (?, ?, ?)";
                try (PreparedStatement pstmt = con.prepareStatement(sql)) {

                    pstmt.setString(1, NumOrden);
                    pstmt.setString(2, NumTarea);
                    pstmt.setString(3, NumEmpleado);

                    // Ejecutar la inserción
                    int filasAfectadas = pstmt.executeUpdate();
                    if (filasAfectadas > 0) {
                        System.out.println("Inserción exitosa.");
                    } else {
                        System.out.println("No se pudo insertar el registro.");
                    }
                }
            } else {
                System.out.println("No se encontró el NumEmpleado para el empleado seleccionado.");
            }
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_cmdIngresarActionPerformed

