        private void llenarCombOrden() {
    try {
        cmbOrden.removeAllItems();

        // Establecer la conexión con la base de datos (ajustar la URL, usuario y contraseña)
        Connection con = Conexion.getConexion();

        String consulta = "SELECT NumOrden, Descripcion FROM OrdenTrabajo";
        try (PreparedStatement pst = con.prepareStatement(consulta)) {
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                String numOrden = rs.getString("NumOrden");
                cmbOrden.addItem(numOrden);
            }
        }

        con.close();
    } catch (SQLException e) {
        e.printStackTrace();
    }
}
    private void cmbOrdenItemStateChanged(java.awt.event.ItemEvent evt) {
    if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED) {
        // Obtiene el número de orden seleccionado
        String numOrdenSeleccionado = cmbOrden.getSelectedItem().toString();

        try {
            // Establecer la conexión con la base de datos (ajustar la URL, usuario y contraseña)
            Connection con = Conexion.getConexion();

            // Consulta para obtener la descripción de la orden seleccionada
            String consulta = "SELECT Descripcion FROM OrdenTrabajo WHERE NumOrden = ?";
            try (PreparedStatement pst = con.prepareStatement(consulta)) {
                pst.setString(1, numOrdenSeleccionado);
                ResultSet rs = pst.executeQuery();

                if (rs.next()) {
                    String descripcion = rs.getString("Descripcion");

                    // Muestra la descripción en el campo de texto txtDescripcion
                    txtDescripcion.setText(descripcion);
                }
            }

            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
}
    
    
